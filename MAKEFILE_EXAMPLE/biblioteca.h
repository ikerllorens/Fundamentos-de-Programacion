/**
 * <brief>Resta dos números enteros</brief>
 * <param name="a">Número del cual se restará</param>
 * <param name="b">Número a restar</param>
 * <return>La resta de a - b</return>
 */
int resta(int a, int b);

/**
 * <brief>Suma tres números enteros</brief>
 * <param name="a">Primer número a sumar</param>
 * <param name="b">Segundo número a sumar</param>
 * <param name="c">Tercer número a sumar</param>
 * <return>La suma de a + b + c</return>
 */
int suma(int a, int b, int c);

/**
 * <brief>Multiplica dos números enteros (Ejemplo con formato XML)</brief>
 * <param name="a">Primer número a multiplicar</param>
 * <param name="b">Segundo número a multiplicar</param>
 * <return>El producto de a * b</return>
 */
int multiplicar(int a, int b);