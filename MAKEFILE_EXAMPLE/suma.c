/**
 * @brief Suma tres números enteros
 * 
 * @param a Primer número a sumar
 * @param b Segundo número a sumar
 * @param c Tercer número a sumar
 * @return La suma de a + b + c
 */
int suma(int a, int b, int c)
{
    return a + b + c;
}
