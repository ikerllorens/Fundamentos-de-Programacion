/**
 * <brief>Resta dos números enteros</brief>
 * 
 * <param name="a">Número del cual se restará</param>
 * <param name="b">Número a restar</param>
 * <return>La resta de a - b</return>
 */
int resta(int a, int b)
{
    return a - b;
}